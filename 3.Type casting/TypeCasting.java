class TypeCasting{
	public static void main(String args[]){
		// int to double convertion
		int i =100000;
		double d = (double)i;
		long l = (long)i;
		short s = (short)i;
		float f = (float)i;
		System.out.println("Integer value: "+i);
		System.out.println("Integer to double: "+d);
		System.out.println("Integer to long: "+l);
		System.out.println("Integer to short: "+s);
		System.out.println("Integer to float: "+f);


		// double to int convertion
		double d1 = 20.25454578424364;
		int i1 = (int)d1;
		long l1 = (long)d1;
		short s1 = (short)d1;
		float f1 = (float)d1;
		System.out.println("Double value: "+d1);
		System.out.println("Double to integer: "+i1);
		System.out.println("Double to long: "+l1);
		System.out.println("Double to short: "+s1);
		System.out.println("Double to float: "+f1);



	}
}