class AutoBoxing{
    public static void main(String args[]){
        int a = 50;
        Integer a1 = a;
        char c = 'M';
        Character c1 = c;
        System.out.println("The value of a is: "+ a);
        System.out.println("The autoboxing of a is: "+a1);
        System.out.println("The value of c is: "+c);
        System.out.println("The autoboxing of c is: "+c1);


    }
}