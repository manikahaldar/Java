class NumberSystem{
    public static void main(String args[]){
        int decimal = 250;
        int binary = 0B11111010;           // for execute the programe in binary code starts from(0B)
        int octal = 0372;                // for execute the programe in octal code starts from(0)
        int hexadecimal = 0xFA;               // for execute the programe in hexadecimal code starts from(0x)

        System.out.println("Decimal:"+decimal);
        System.out.println("Binary to Decimal:"+binary);
        System.out.println("Octal to decimal Decimal:"+octal);
        System.out.println("Hexadecimal to Decimal:"+hexadecimal);

        


    }
}